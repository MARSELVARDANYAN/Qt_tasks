//Data Types and Variables

#include <iostream>

int main()
{
    int INT = 13;
    short SHORT = 13;
    float FLOAT = 3.3;
    double DOUBLE = 13.3;
    char CHAR = 'a';
    bool BOOL = true;

    std::cout << "INT = " << INT << " " 
              << "SHORT = " << SHORT << " "
              << "FLOAT = " << FLOAT << " "
              << "DOUBLE = " << DOUBLE << " "
              << "CHAR = " << CHAR  << " "
              << "BOOL = " << BOOL << std::endl;

    return 0;
}
